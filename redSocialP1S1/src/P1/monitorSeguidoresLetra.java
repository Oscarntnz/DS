    private int calcularNumSeguidoresLetra (Usuario usuario, String letra){

	ArrayList<Usuario> seguidores = usuario.getSeguidores();
	int numSeguidoresL = 0;

	for(int i=0; i < seguidores.size(); i++){
		if(seguidores.get(i).getNombre().startsWith(letra))
                    numSeguidoresL ++;
	}

	return numSeguidoresL;
    }

